'use strict';

const { buildSubgraphSchema, printSubgraphSchema } = require('@apollo/subgraph');
const { loadFilesSync } = require('@graphql-tools/load-files');
const { mergeTypeDefs } = require('@graphql-tools/merge');
const { join } = require('path');
const fs = require('fs');

const ROOT = join(__dirname, '..');
const OUTPUT_DIR = join(ROOT, 'generated.sdl');

const SCHEMAS = [
    {
        name: 'v1',
        schemasDir: join(ROOT, 'src', 'graphql', 'schemas'),
        outputFile: join(OUTPUT_DIR, 'graphql_v1.graphql')
    },
    {
        name: 'v2',
        schemasDir: join(ROOT, 'src', 'graphqlv2', 'schemas'),
        outputFile: join(OUTPUT_DIR, 'graphql_v2.graphql')
    }
];

const AUTO_GENERATED_HEADER =
    '# This file is auto-generated by generatorScripts/generateGraphqlSdl.js so do not edit manually\n\n';

const isCheckMode = process.argv.includes('--check');

let hasErrors = false;

for (const { name, schemasDir, outputFile } of SCHEMAS) {
    const typesArray = loadFilesSync(schemasDir, { recursive: true });
    const typeDefs = mergeTypeDefs(typesArray);
    const schema = buildSubgraphSchema({ typeDefs, resolvers: {} });
    const sdl = AUTO_GENERATED_HEADER + printSubgraphSchema(schema);

    if (isCheckMode) {
        if (!fs.existsSync(outputFile)) {
            console.error(`FAIL: ${outputFile} does not exist. Run "make graphql-sdl" to generate it.`);
            hasErrors = true;
            continue;
        }
        const existing = fs.readFileSync(outputFile, 'utf-8');
        if (existing !== sdl) {
            console.error(`FAIL: ${name} SDL snapshot is out of date. Run "make graphql-sdl" to update it.`);
            hasErrors = true;
        } else {
            console.log(`OK: ${name} SDL snapshot is up to date.`);
        }
    } else {
        if (!fs.existsSync(OUTPUT_DIR)) {
            fs.mkdirSync(OUTPUT_DIR, { recursive: true });
        }
        fs.writeFileSync(outputFile, sdl);
        console.log(`Generated ${outputFile}`);
    }
}

if (hasErrors) {
    process.exit(1);
}
